 Фиксируем пакеты в requirements.txt
 перейдите в директорию, в которой вы работаете с автотестами
 После чего выполните в терминале команду:

 pip freeze > requirements.txt
 Эта команда сохранит все версии пакетов в специальный файл requirements.txt

 Теперь в новом проекте/переустановки системы, копируем файл requirements.txt и команду в терминале
 pip install -r requirements.txt
 Все пакеты установлены одной командой